namespace Hardstuck.GuildWars2.MumbleLink
{
    /// <summary>
    /// A vector with three coordinates
    /// </summary>
    public struct Vector3D
    {
        /// <summary>
        /// The representation on the X axis
        /// </summary>
        public float X { get; set; }

        /// <summary>
        /// The representation on the Y axis
        /// </summary>
        public float Y { get; set; }

        /// <summary>
        /// The representation on the Z axis
        /// </summary>
        public float Z { get; set; }
    }
}
